#pragma once

#ifndef Window_h__
#define Window_h__

#include "IKeyboard.h"

class IWindow
{
public:
  IWindow(){};
  virtual ~IWindow(){};

  /// Установить текущий контекст для данного окна.
  virtual void SetCurrentContext() = 0;

  /// Должно ли окно закрыться?
  virtual bool WindowShouldClose() = 0;

  /// Переключить буферы.
  virtual void SwapBuffers() = 0;

  /// Получить клавиатуру.
  virtual const IKeyboard &GetKeyboard() = 0;
};

#endif // Window_h__
